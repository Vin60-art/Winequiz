function shuffle(arr){for(let i=arr.length-1;i>0;i--){const j=Math.floor(Math.random()*(i+1));[arr[i],arr[j]]=[arr[j],arr[i]]}}
let current=0,score=0;
const qEl=document.getElementById("question");
const pEl=document.getElementById("progress");
const optEl=document.getElementById("options");
const fb=document.getElementById("feedback");
const fbTxt=document.getElementById("fbtxt");
const nextBtn=document.getElementById("next");
const res=document.getElementById("result");
const scoreEl=document.getElementById("score");
const restart=document.getElementById("restart");

function renderQuestion(){
  const q=questions[current];
  pEl.textContent=`Вопрос ${current+1} из ${questions.length}`;
  qEl.textContent=q.text;
  optEl.innerHTML="";
  q.options.forEach((opt,i)=>{
    const b=document.createElement("button");
    b.textContent=opt;
    b.onclick=()=>selectAnswer(i);
    optEl.appendChild(b);
  });
  fb.classList.add("hidden");
}

function selectAnswer(idx){
  const q=questions[current];
  const buttons=optEl.querySelectorAll("button");
  buttons.forEach(b=>b.disabled=true);
  if(idx===q.correct){
    buttons[idx].classList.add("correct");
    fbTxt.textContent="Правильно!";
    score++;
  }else{
    buttons[idx].classList.add("incorrect");
    buttons[q.correct].classList.add("correct");
    fbTxt.innerHTML=`Неправильно. <em>${q.explanation}</em>`;
  }
  fb.classList.remove("hidden");
}

nextBtn.onclick=()=>{
  current++;
  if(current<questions.length){
    renderQuestion();
  }else{
    finishQuiz();
  }
};

function finishQuiz(){
  qEl.textContent="";
  pEl.textContent="";
  optEl.innerHTML="";
  fb.classList.add("hidden");
  scoreEl.textContent=`Вы ответили правильно на ${score} из ${questions.length} вопросов.`;
  res.classList.remove("hidden");
}

restart.onclick=()=>{
  shuffle(questions);
  current=0;score=0;
  res.classList.add("hidden");
  renderQuestion();
};

window.addEventListener("DOMContentLoaded",()=>{
  shuffle(questions);
  renderQuestion();
});
